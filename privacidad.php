        <h1>Política de Privacidad</h1>
        
        <p><strong>Fecha de vigencia:</strong> Diciembre de 2025</p>
        <p>En Lab-Explora ("nosotros", "nuestro", "la Plataforma"), respetamos y protegemos la privacidad de nuestros usuarios. Esta Política de Privacidad describe de manera clara y transparente cómo recopilamos, utilizamos, almacenamos, compartimos y protegemos la información personal que usted nos proporciona al utilizar nuestros servicios.</p>
        <p>Al acceder o utilizar Lab-Explora, usted acepta las prácticas descritas en esta Política de Privacidad. Le recomendamos leerla detenidamente y consultarla periódicamente para estar informado sobre cualquier actualización.</p>

        <h2>1. Información que Recopilamos</h2>
        <h3>1.1 Información Proporcionada Directamente por el Usuario</h3>
        <p>Recopilamos información que usted proporciona voluntariamente al registrarse, crear una cuenta o interactuar con la Plataforma:</p>
        <ul>
            <li><strong>Datos de registro:</strong> nombre completo, dirección de correo electrónico, contraseña encriptada</li>
            <li><strong>Datos de perfil:</strong> foto de perfil, información biográfica adicional que decida compartir</li>
            <li><strong>Datos de autenticación de dos factores (2FA):</strong> secreto compartido para generación de códigos OTP (almacenado de forma encriptada), códigos de respaldo</li>
            <li><strong>Contenido generado:</strong> publicaciones científicas, comentarios, valoraciones, reportes</li>
            <li><strong>Interacciones:</strong> publicaciones guardadas, historial de "me gusta", actividad de lectura</li>
            <li><strong>Comunicaciones:</strong> mensajes enviados a soporte, reportes de contenido</li>
        </ul>

        <h3>1.2 Información Recopilada Automáticamente</h3>
        <p>Cuando utiliza Lab-Explora, recopilamos automáticamente cierta información técnica y de uso:</p>
        <ul>
            <li><strong>Datos de navegación:</strong> dirección IP, tipo y versión del navegador, sistema operativo, idioma del navegador</li>
            <li><strong>Datos de sesión:</strong> cookies de sesión, tokens de autenticación, preferencias de usuario</li>
            <li><strong>Actividad en la Plataforma:</strong> páginas visitadas, publicaciones leídas, tiempo de permanencia, patrones de uso</li>
            <li><strong>Datos de dispositivo:</strong> identificadores únicos del dispositivo, resolución de pantalla, zona horaria</li>
            <li><strong>Registros del sistema (logs):</strong> accesos, errores, eventos de seguridad</li>
        </ul>

        <h3>1.3 Información de Terceros</h3>
        <p>Actualmente, Lab-Explora no integra servicios de redes sociales ni recopila información de fuentes externas. En caso de implementarse en el futuro, se actualizará esta política y se notificará a los usuarios.</p>

        <h2>2. Finalidades del Tratamiento de Datos</h2>
        <p>Utilizamos la información personal recopilada para las siguientes finalidades:</p>
        
        <h3>2.1 Operación y Gestión de la Plataforma</h3>
        <ul>
            <li>Crear, gestionar y mantener su cuenta de usuario</li>
            <li>Autenticar su identidad y proporcionar acceso seguro mediante contraseñas y 2FA</li>
            <li>Procesar y almacenar su contenido (publicaciones, comentarios, interacciones)</li>
            <li>Generar credenciales digitales personalizadas en formato PDF</li>
            <li>Facilitar la comunicación con otros usuarios a través de comentarios</li>
        </ul>

        <h3>2.2 Seguridad y Moderación</h3>
        <ul>
            <li>Detectar, prevenir y responder a fraude, abuso o actividades sospechosas</li>
            <li>Moderar contenido mediante sistemas automatizados de inteligencia artificial</li>
            <li>Gestionar y resolver reportes de contenido inapropiado</li>
            <li>Proteger la seguridad de la Plataforma y sus usuarios</li>
            <li>Cumplir con obligaciones legales y regulatorias</li>
        </ul>

        <h3>2.3 Mejora de Servicios</h3>
        <ul>
            <li>Analizar el uso de la Plataforma para mejorar la experiencia del usuario</li>
            <li>Desarrollar nuevas funcionalidades y optimizar las existentes</li>
            <li>Realizar análisis estadísticos y métricas de rendimiento (de forma anónima y agregada)</li>
            <li>Entrenar y mejorar algoritmos de moderación automática</li>
        </ul>

        <h3>2.4 Comunicación</h3>
        <ul>
            <li>Enviar notificaciones sobre su cuenta, actualizaciones de seguridad o cambios en nuestros términos</li>
            <li>Responder a solicitudes de soporte técnico</li>
            <li>Comunicar información importante sobre la Plataforma</li>
        </ul>

        <h2>3. Base Legal del Tratamiento</h2>
        <p>El tratamiento de sus datos personales se fundamenta en:</p>
        <ul>
            <li><strong>Consentimiento:</strong> Al registrarse y utilizar la Plataforma usted otorga su consentimiento expreso</li>
            <li><strong>Ejecución del contrato:</strong> El procesamiento es necesario para proporcionar los servicios solicitados</li>
            <li><strong>Obligación legal:</strong> Cumplimiento de obligaciones legales aplicables</li>
            <li><strong>Interés legítimo:</strong> Seguridad de la Plataforma, prevención de fraude, mejora de servicios</li>
        </ul>

        <h2>4. Cookies y Tecnologías Similares</h2>
        <p>Lab-Explora utiliza cookies y tecnologías similares para mejorar la funcionalidad y experiencia del usuario:</p>
        
        <h3>4.1 Tipos de Cookies</h3>
        <ul>
            <li><strong>Cookies esenciales:</strong> Necesarias para el funcionamiento básico (sesión de usuario, autenticación)</li>
            <li><strong>Cookies de preferencias:</strong> Almacenan configuraciones y preferencias de usuario</li>
            <li><strong>Cookies de seguridad:</strong> Protegen contra accesos no autorizados y fraude</li>
        </ul>

        <h3>4.2 Gestión de Cookies</h3>
        <p>Puede configurar su navegador para rechazar todas o algunas cookies. Sin embargo, esto puede afectar la funcionalidad completa de la Plataforma. Las cookies esenciales no pueden deshabilitarse sin comprometer el acceso a su cuenta.</p>

        <h2>5. Autenticación de Dos Factores (2FA)</h2>
        <h3>5.1 Datos Recopilados</h3>
        <p>Para publicadores y administradores que utilizan 2FA obligatoriamente:</p>
        <ul>
            <li>Secreto compartido (almacenado encriptado en base de datos)</li>
            <li>Códigos de respaldo generados (encriptados)</li>
            <li>Registro de accesos autenticados (fecha, hora, IP)</li>
        </ul>

        <h3>5.2 Protección de Datos de 2FA</h3>
        <p>Los secretos de 2FA se almacenan utilizando cifrado AES-256. No tenemos acceso a sus códigos OTP de un solo uso, que se generan localmente en su aplicación autenticadora. Los códigos de respaldo se almacenan con hash criptográfico irreversible.</p>

        <h2>6. Credenciales Digitales PDF</h2>
        <h3>6.1 Información Contenida</h3>
        <p>Las credenciales digitales generadas incluyen:</p>
        <ul>
            <li>Nombre del usuario</li>
            <li>Correo electrónico</li>
            <li>ID único de usuario</li>
            <li>Foto de perfil (si está disponible)</li>
            <li>Firma digital criptográfica (hash SHA-256 único)</li>
            <li>Fecha de emisión y período de validez</li>
        </ul>

        <h3>6.2 Privacidad de las Credenciales</h3>
        <p>Las credenciales PDF se generan dinámicamente en el momento de la descarga y no se almacenan permanentemente en nuestros servidores. La firma digital permite verificar la autenticidad del documento pero no contiene información personalidentificable adicional.</p>

        <h2>7. Compartir Información con Terceros</h2>
        <p>Lab-Explora <strong>no vende, alquila ni comercializa</strong> su información personal a terceros. Únicamente compartimos información en las siguientes circunstancias limitadas:</p>

        <h3>7.1 Proveedores de Servicios</h3>
        <p>Podemos compartir información con proveedores de confianza que nos ayudan a operar la Plataforma:</p>
        <ul>
            <li>Servicios de hosting y almacenamiento en la nube</li>
            <li>Servicios de envío de correos electrónicos transaccionales</li>
            <li>Proveedores de análisis (datos anónimos y agregados únicamente)</li>
        </ul>
        <p>Todos los proveedores están contractualmente obligados a proteger su información y solo pueden usarla para los fines especificados.</p>

        <h3>7.2 Cumplimiento Legal</h3>
        <p>Podemos divulgar información cuando sea necesario para:</p>
        <ul>
            <li>Cumplir con leyes, regulaciones, procesos legales o solicitudes gubernamentales</li>
            <li>Proteger los derechos, propiedad o seguridad de Lab-Explora, nuestros usuarios o el público</li>
            <li>Detectar, prevenir o investigar fraude, violaciones de seguridad o actividades ilegales</li>
        </ul>

        <h3>7.3 Transferencias Corporativas</h3>
        <p>En caso de fusión, adquisición o venta de activos, su información personal puede transferirse. Le notificaremos mediante correo electrónico y/o aviso destacado en la Plataforma antes de que su información se transfiera y quede sujeta a una política de privacidad diferente.</p>

        <h2>8. Moderación Automatizada con Inteligencia Artificial</h2>
        <p>Lab-Explora utiliza sistemas automatizados de inteligencia artificial para moderar contenido:</p>
        <ul>
            <li><strong>Análisis de texto:</strong> Detección de contenido inapropiado, spam, lenguaje ofensivo</li>
            <li><strong>Procesamiento automatizado:</strong> Las publicaciones pueden ser automáticamente aprobadas, rechazadas o marcadas para revisión manual</li>
            <li><strong>Datos procesados:</strong> Contenido textual de publicaciones, comentarios, metadatos</li>
        </ul>
        <p><strong>Sus derechos:</strong> Puede solicitar revisión manual de cualquier decisión automatizada que afecte significativamente su cuenta.</p>

        <h2>9. Conservación de Datos</h2>
        <p>Conservamos su información personal durante el tiempo que:</p>
        <ul>
            <li>Su cuenta permanezca activa</li>
            <li>Sea necesario para proporcionar nuestros servicios</li>
            <li>Sea requerido para cumplir con obligaciones legales (por ejemplo, registros fiscales, auditorías)</li>
            <li>Resolver disputas y hacer cumplir nuestros acuerdos</li>
        </ul>
        <p>Los datos de sesión y registros técnicos se conservan por períodos limitados (generalmente 90-180 días) para fines de seguridad y análisis.</p>

        <h2>10. Seguridad de la Información</h2>
        <p>Implementamos medidas técnicas y organizativas para proteger su información personal:</p>
        
        <h3>10.1 Medidas Técnicas</h3>
        <ul>
            <li>Cifrado de contraseñas mediante hash bcrypt con salt</li>
            <li>Cifrado de datos sensibles en tránsito (HTTPS/TLS)</li>
            <li>Cifrado AES-256 para secretos de 2FA</li>
            <li>Tokens de sesión seguros con expiración automática</li>
            <li>Firewall de aplicaciones web (WAF)</li>
            <li>Protección contra inyección SQL y XSS</li>
        </ul>

        <h3>10.2 Medidas Organizativas</h3>
        <ul>
            <li>Acceso restringido a datos personales solo para personal autorizado</li>
            <li>Auditorías de seguridad periódicas</li>
            <li>Capacitación en protección de datos para el personal</li>
            <li>Políticas de gestión de incidentes de seguridad</li>
        </ul>

        <p><strong>Importante:</strong> Ningún sistema es 100% seguro. Aunque implementamos medidas razonables, no podemos garantizar la seguridad absoluta de su información. Le recomendamos utilizar contraseñas fuertes y únicas, activar 2FA y no compartir sus credenciales con terceros.</p>

        <h2>11. Sus Derechos (Derechos ARCO)</h2>
        <p>Conforme a la legislación aplicable, usted tiene los siguientes derechos respecto a sus datos personales:</p>

        <h3>11.1 Acceso</h3>
        <p>Derecho a saber qué datos personales tenemos sobre usted y solicitar una copia de los mismos.</p>

        <h3>11.2 Rectificación</h3>
        <p>Derecho a corregir datos inexactos o incompletos. Puede actualizar su información directamente desde su perfil.</p>

        <h3>11.3 Cancelación</h3>
        <p>Derecho a solicitar la eliminación de sus datos personales cuando ya no sean necesarios para las finalidades para las cuales fueron recopilados.</p>

        <h3>11.4 Oposición</h3>
        <p>Derecho a oponerse al tratamiento de sus datos para determinadas finalidades.</p>

        <h3>11.5 Portabilidad</h3>
        <p>Derecho a recibir sus datos personales en un formato estructurado, de uso común y lectura mecánica.</p>

        <h3>11.6 Revocación del Consentimiento</h3>
        <p>Derecho a retirar su consentimiento en cualquier momento, sin afectar la licitud del tratamiento previo.</p>

        <h3>11.7 Ejercicio de Derechos</h3>
        <p>Para ejercer cualquiera de estos derechos, puede:</p>
        <ul>
            <li>Acceder a la configuración de su perfil en la Plataforma</li>
            <li>Enviar una solicitud por correo electrónico a: <strong>privacidad@lab-explora.com</strong></li>
            <li>Utilizar el formulario de contacto disponible en la Plataforma</li>
        </ul>
        <p>Responderemos a su solicitud en un plazo máximo de 20 días hábiles.</p>

        <h2>12. Privacidad de Menores</h2>
        <p>Lab-Explora no está dirigida a menores de 18 años. No recopilamos intencionalmente información personal de menores. Si descubrimos que hemos recopilado información de un menor sin el consentimiento de sus padres o tutores, eliminaremos esa información inmediatamente.</p>
        <p>Si usted es padre o tutor y cree que su hijo nos ha proporcionado información personal, por favor contáctenos de inmediato.</p>

        <h2>13. Transferencias Internacionales</h2>
        <p>Actualmente, toda la información se almacena y procesa en servidores ubicados en [especificar ubicación]. En caso de transferencias internacionales futuras, implementaremos salvaguardas apropiadas (cláusulas contractuales estándar, certificaciones de adecuación) para proteger su información.</p>

        <h2>14. Cambios a esta Política de Privacidad</h2>
        <p>Nos reservamos el derecho de actualizar esta Política de Privacidad periódicamente para reflejar cambios en nuestras prácticas, tecnología, requisitos legales u otros factores.</p>
        <p>Le notificaremos sobre cambios materiales mediante:</p>
        <ul>
            <li>Aviso destacado en la Plataforma</li>
            <li>Correo electrónico enviado a la dirección registrada en su cuenta</li>
            <li>Actualización de la fecha de "Última actualización" en la parte superior de esta política</li>
        </ul>
        <p>Le recomendamos revisar esta política periódicamente. El uso continuado de la Plataforma después de la publicación de cambios constituirá su aceptación de los mismos.</p>

        <h2>15. Legislación Aplicable</h2>
        <p>Esta Política de Privacidad se rige por las leyes de protección de datos de México, incluyendo la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (LFPDPPP) y su Reglamento.</p>

